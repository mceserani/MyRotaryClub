Mese = function (mese) {
	var month = new Array();
	month[0] = "Gennaio";
	month[1] = "Febbraio";
	month[2] = "Marzo";
	month[3] = "Aprile";
	month[4] = "Maggio";
	month[5] = "Giugno";
	month[6] = "Luglio";
	month[7] = "Agosto";
	month[8] = "Settembre";
	month[9] = "Ottobre";
	month[10] = "Novembre";
	month[11] = "Dicembre";
	return month[mese];
}

